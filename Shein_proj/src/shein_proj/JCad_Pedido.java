     private void fetchData() {
        String query = """
                       SELECT id_pedido,id_cliente,data_pedido, calcularTotalPedido(id_pedido) AS total_calculado
                       FROM Pedido;""";
        try (PreparedStatement statement = connection.prepareStatement(query);
             ResultSet resultSet = statement.executeQuery()) {
            
             DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
             model.setRowCount(0);

            // Iterate over the result set and add rows to the table model
            while (resultSet.next()) {
                int id_produto = resultSet.getInt("id_pedido");
                int id_cliente = resultSet.getInt("id_cliente");
                Date data = resultSet.getDate("data_pedido");
                float total = resultSet.getFloat("total_calculado");

                model.addRow(new Object[]{id_produto, id_cliente, data, total});
                System.out.println(model);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    

    private void Salvar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Salvar_btnActionPerformed
        // TODO add your handling code here:
        String query = "INSERT INTO produto (id_produto, nome_pro, categoria_pro, preco, quantidade_estoque) " +
        "VALUES (DEFAULT,?, ?, ?, ?)";
        try (PreparedStatement statement = connection.prepareStatement(query)) {
            String produto_texto,categoria_texto ;
            int quantidade_texto;
            float preco_texto;
            produto_texto = produto_txt.getText();
            categoria_texto = categoria_txt.getText();
            preco_texto = Float.parseFloat(preco_txt.getText());
            quantidade_texto = Integer.parseInt(quantidade_txt.getText());
            statement.setString(1, produto_texto);
            statement.setString(2, categoria_texto);
            statement.setFloat(3, preco_texto);
            statement.setInt(4, quantidade_texto);
            

            statement.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null, "Added product !! ");
            
            fetchData();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error adding client: " + ex.getMessage());
        }
    }//GEN-LAST:event_Salvar_btnActionPerformed

    private void Excluir_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Excluir_btnActionPerformed
        // TODO add your handling code here:       
            String query = "DELETE FROM Produto WHERE id_produto = ?";
        try (PreparedStatement statement = connection.prepareStatement(query)) {
            int id = Integer.parseInt(JOptionPane.showInputDialog("Enter product ID to delete:"));
            statement.setInt(1, id);

            int rowsAffected = statement.executeUpdate();
            
            fetchData();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null,"Product deleted successfully");
            } else {
                JOptionPane.showMessageDialog(null,"Product not found");
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error deleting product: " + ex.getMessage());
        }
    }//GEN-LAST:event_Excluir_btnActionPerformed


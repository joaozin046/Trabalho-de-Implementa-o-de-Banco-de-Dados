    private void fetchData() {
        String query = "SELECT * FROM Produto";
        try (PreparedStatement statement = connection.prepareStatement(query);
             ResultSet resultSet = statement.executeQuery()) {
            
             DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
             model.setRowCount(0);

            // Iterate over the result set and add rows to the table model
            while (resultSet.next()) {
                int id = resultSet.getInt("id_produto");
                String name = resultSet.getString("nome_pro");
                String categoria = resultSet.getString("categoria_pro");
                float preco = resultSet.getFloat("preco");
                int quant = resultSet.getInt("quantidade_estoque");

                model.addRow(new Object[]{id, name, categoria, preco, quant});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
